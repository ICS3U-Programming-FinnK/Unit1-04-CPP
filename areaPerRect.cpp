// This program calculates and displays the area and
// perimeter of a rectangle with dimensions 3cm X 3cm.
#include <iostream>

int main() {
    std::cout << "For a rectangle that has dimensions\n";
    std::cout << "length 3cm and width 3cm:\n";
    std::cout << "\n";
    std::cout << "Area = " << 3 * 3 << " cm^2\n";
    std::cout << "Perimeter = " << 2 * (3 + 3) << " cm\n";
}