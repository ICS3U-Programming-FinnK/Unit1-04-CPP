#include <cmath>
#include <iostream>

int main() {
    std::cout << "8 + 2 = " << 8 + 2 << std::endl;
    std::cout << "7 - 1 = " << 7 - 1 << std::endl;
    std::cout << "6 / 2 = " << 6 / 2 << std::endl;
    std::cout << "8 - 2 / 2 = " << 8 - 2 / 2 << std::endl;
    std::cout << "283 = " << pow(2, 8) << std::endl;
    std::cout << "1 / 5 = " << float(1) / float(5) << std::endl;
}